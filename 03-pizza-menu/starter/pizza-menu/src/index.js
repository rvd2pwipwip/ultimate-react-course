function App() {
  return (
    <div className="container">
      <Header />
      <Menu />
      <Footer />
    </div>
  );
}

function Header() {
  return (
    <header className="header">
      <h1 className="header__title">Fast React Pizza Co.</h1>
    </header>
  );
}

function Menu() {
  //   const pizzas = [];
  const pizzas = pizzaData;

  return (
    <main className="menu">
      <h2>Our Menu</h2>
      {pizzas.length > 0 ? (
        <>
          <p>
            Authentic Italian cuisine. 6 creative dishes to choose from. All
            from our stone oven, all organic, all delicious.
          </p>
          <ul className="pizzas">
            {pizzas.map((pizza) => (
              <Pizza key={pizza.name} {...pizza} />
            ))}
          </ul>
        </>
      ) : (
        <p>We're still working on our menu. Please come back later :)</p>
      )}
    </main>
  );
}

function Pizza(props) {
  return (
    <li className={props.soldOut ? "pizza sold-out" : "pizza"}>
      <img src={props.photoName} alt={props.name} />
      <div className="pizza__details">
        <h2>{props.name}</h2>
        <p>{props.ingredients}</p>
        <span>{props.price}</span>
        <p>{props.soldOut ? "Sold Out" : "Available"}</p>
      </div>
    </li>
  );
}

function Footer() {
  const hour = new Date().getHours();
  const openHour = 10;
  const closeHour = 22;
  const isOpen = hour >= openHour && hour <= closeHour;

  return (
    <div className="order">
      <footer className="footer">
        {isOpen
          ? `We're open until ${closeHour}h00. Come visit us or order online!`
          : `We're happy to welcome you between ${openHour}h00 and ${closeHour}h00.`}
      </footer>
      <button className="btn">Order</button>
    </div>
  );
}

const root = ReactDOM.createRoot(document.getElementById("root"));
root.render(
  <React.StrictMode>
    <App />
  </React.StrictMode>
);

